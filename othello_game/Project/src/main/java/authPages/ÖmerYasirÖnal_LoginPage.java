    /**
     * Bu method, kullanıcın girmiş olduğu email ve şifre değerlerini kontrol
     * eder. Eğer kullanıcı uygulamaya kayıtlı ise uygulamaya giriş yapar ve
     * başlangıç ekranına yönlendirilir. Kayıtlı değil ise "Kayıt ol" buttonuna
     * tıklayarak kayıt ekranına gider.
     */
    private void btnGirişYapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirişYapActionPerformed

        String mail1 = txtMail.getText();
        String password1 = new String(txtSifre.getPassword());
        String mail2 = txtMail_2.getText();
        String password2 = new String(txtSifre_2.getPassword());
        boolean found1 = login(mail1, password1);
        boolean found2 = login(mail2, password2);

        if (mail1.isEmpty() || password1.isEmpty() || mail2.isEmpty() || password2.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Boş alan bırakmayınız!", "Hata", JOptionPane.INFORMATION_MESSAGE);
        } else if (mail1.equals(mail2) && found1 && found2) {
            JOptionPane.showMessageDialog(null, "İki kullanıcı aynı olamaz", "Hata", JOptionPane.INFORMATION_MESSAGE);
        } else if (found1 && found2) {
            JOptionPane.showMessageDialog(null, "Giriş başarılı!", "Başarılı", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            ÖmerYasirÖnal_StartPage startPage = new ÖmerYasirÖnal_StartPage(mail1, mail2, this);
            txtMail.setText("");
            txtMail_2.setText("");
            txtSifre.setText("");
            txtSifre_2.setText("");
            startPage.setVisible(true);
        } else if (!found1) {
            JOptionPane.showMessageDialog(null, "1. Kullanıcının mail adresi veya şifresi hatalı!", "Hata", JOptionPane.ERROR_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(null, "2. Kullanıcının mail adresi veya şifresi hatalı!", "Hata", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGirişYapActionPerformed

