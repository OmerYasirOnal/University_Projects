    /**
     * Bu method, kullanıcın bilgilerini kontrol ederek kayıtlı olup olmadığını
     * kontrol ederek girişin başarılı olup olmadığını kontrol eder.
     */
    public boolean login(String mail, String password) {

        boolean found;

        try {
            File file = new File("users.txt");
            try (Scanner scanner = new Scanner(file)) {
                while (scanner.hasNextLine()) {
                    String line = scanner.nextLine();
                    String[] parts = line.split(",");
                    if (parts.length >= 2) {
                        String fileMail = parts[0].trim();
                        String filePassword = parts[2].trim();
                        if (mail.equals(fileMail) && password.equals(filePassword)) {
                            found = true;
                            return found;

                        }
                    }
                }
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }

        return false;

    }
